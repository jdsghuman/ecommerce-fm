import Link from 'next/link'
import type { GetStaticProps, NextPage } from 'next'
import Head from 'next/head'
import Header from '@components/Header'
import Container from '@components/Container'
import useHasMounted from '@components/hooks/useHasMounted'
import markets from '@data/markets.json'
import Footer from '@components/Footer'
import Carousel from '@components/Carousel'
import PostItem from '@components/PostItem/PostItem'

import styles from '@styles/Home.module.scss'

export const getStaticProps: GetStaticProps = async () => {
  const vendors = markets
  return {
    props: {
      vendors,
    },
  }
}
const Home: NextPage = ({ vendors }: any) => {
  // console.log('markets', markets)
  const hasMounted = useHasMounted()
  if (!hasMounted) {
    return null
  }

  return (
    <div className={styles.container}>
      <Head>
        <title>ecommerce-fm</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Header />

      <Container className={styles.padding}>
        <h3 className={styles.heading}>Featured vendors</h3>
        <Carousel featuredPosts={vendors} />
        <main className={styles.main}>
          <h3 className={styles.heading}>Shop at our vendors</h3>
          <ul className={styles.products}>
            {vendors.map((vendor: any) => {
              return (
                <PostItem
                  id={vendor.id}
                  image={vendor.image}
                  title={vendor.title}
                  description={vendor.description}
                  slug={vendor.slug}
                />
              )
            })}
          </ul>
        </main>
      </Container>
      <Footer />
    </div>
  )
}

export default Home
